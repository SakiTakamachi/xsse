#include <cmocka.h>

#include "../src/xsse.h"

static void test_mm_or_si128(void **state)
{
	(void) state;

	__m128i a = _mm_setr_epi8(
		0x00, 0x0F, 0xF0, 0xFF, 0x00, 0x0F, 0xF0, 0xFF,
		0x00, 0x0F, 0xF0, 0xFF, 0x00, 0x0F, 0xF0, 0xFF
	);
	__m128i b = _mm_setr_epi8(
		0x00, 0x00, 0x00, 0x00, 0x0F, 0x0F, 0x0F, 0x0F,
		0xF0, 0xF0, 0xF0, 0xF0, 0xFF, 0xFF, 0xFF, 0xFF
	);

	__m128i result = _mm_or_si128(a, b);

	int8_t expected[16] = {
		0x00, 0x0F, 0xF0, 0xFF, 0x0F, 0x0F, 0xFF, 0xFF,
		0xF0, 0xFF, 0xF0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF
	};
	int8_t actual[16];
	_mm_storeu_si128((__m128i *) actual, result);

	for (int i = 0; i < 16; i++) {
		assert_int_equal(actual[i], expected[i]);
	}
}

